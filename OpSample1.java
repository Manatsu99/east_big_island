class OpSample1 {
   public static void main(String[] args){
      int a = 7;
      int b = 3;
      int c = a * b;
      
      System.out.println(a + 3);
      System.out.println(a - b);
      System.out.println(c);
      System.out.println(7 / 3);
      System.out.println(7 % 3);
      
      System.out.println(10.0 + 3);
      System.out.println(10.0 / 3);
      System.out.println(10.0 % 3);
      System.out.println();
      System.out.println();
   }
}